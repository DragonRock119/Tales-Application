function mostrar(id) {
    if (id == "0-3") {
        $("#0-3").show();
        $("#3-6").hide();
        $("#6-8").hide();
        $("#8-10").hide();
        $("#10-12").hide();
    }

    if (id == "3-6") {
        $("#0-3").hide();
        $("#3-6").show();
        $("#6-8").hide();
        $("#8-10").hide();
        $("#10-12").hide();
    }

    if (id == "6-8") {
        $("#0-3").hide();
        $("#3-6").hide();
        $("#6-8").show();
        $("#8-10").hide();
        $("#10-12").hide();
    }

    if (id == "8-10") {
        $("#0-3").hide();
        $("#3-6").hide();
        $("#6-8").hide();
        $("#8-10").show();
        $("#10-12").hide();
    }

    if (id == "10-12") {
        $("#0-3").hide();
        $("#3-6").hide();
        $("#6-8").hide();
        $("#8-10").hide();
        $("#10-12").show();
    }

}

